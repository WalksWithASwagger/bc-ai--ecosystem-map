const fs = require('fs');

// Read and parse the masterlist
function parseMasterlist() {
  const content = fs.readFileSync('completed-research/archive-2025-07-29/all-organizations-masterlist.md', 'utf8');
  const lines = content.split('\n');
  
  const organizations = [];
  let inDirectory = false;
  
  for (const line of lines) {
    // Start parsing after "Complete Organization Directory"
    if (line.includes('Complete Organization Directory')) {
      inDirectory = true;
      continue;
    }
    
    if (!inDirectory) continue;
    
    // Parse organization entries (numbered format)
    const match = line.match(/^\d+\.\s\*\*(.+?)\*\*\s*-?\s*(.*)$/);
    if (match) {
      const name = match[1].trim();
      const urlPart = match[2].trim();
      const hasUrl = !urlPart.includes('URL needed') && urlPart.length > 0;
      const url = hasUrl ? urlPart : null;
      
      organizations.push({
        name,
        url,
        hasUrl,
        missingFields: []
      });
    }
  }
  
  return organizations;
}

// Analyze organizations and create report
function analyzeOrganizations() {
  const orgs = parseMasterlist();
  console.log(`Parsed ${orgs.length} organizations from masterlist`);
  
  // Since we only have basic data from masterlist, we'll focus on URL completeness
  // and create placeholder analysis for other fields
  
  const report = [];
  
  for (const org of orgs) {
    const data = {
      name: org.name,
      notionUrl: `https://www.notion.so/${org.name.replace(/\s+/g, '-').toLowerCase()}`, // Placeholder
      missingFields: [],
      contactScore: 0,
      impactWeight: 0
    };
    
    // Fields we know are missing based on masterlist
    if (!org.hasUrl) {
      data.missingFields.push('Website');
      data.impactWeight += 20; // Website weight
    }
    
    // For organizations without URLs, we can assume other contact fields are also likely missing
    if (!org.hasUrl) {
      data.missingFields.push('Email (likely)');
      data.missingFields.push('Phone (likely)');
      data.missingFields.push('LinkedIn URL (likely)');
      data.impactWeight += 55; // Combined weight for contact fields
      data.contactScore = 0; // No contact info available
    } else {
      // For orgs with URLs, we don't know their contact completeness from masterlist
      data.missingFields.push('Unknown - requires Notion access');
      data.contactScore = -1; // Unknown
    }
    
    report.push(data);
  }
  
  // Sort by highest impact (missing URLs and likely missing all contact info)
  report.sort((a, b) => b.impactWeight - a.impactWeight);
  
  return {
    totalOrganizations: orgs.length,
    missingUrls: orgs.filter(o => !o.hasUrl).length,
    withUrls: orgs.filter(o => o.hasUrl).length,
    report: report
  };
}

// Generate markdown report
function generateMarkdownReport() {
  const analysis = analyzeOrganizations();
  const date = new Date().toISOString().split('T')[0];
  
  let markdown = `# 🗺️ BC AI Ecosystem - Data Completeness Analysis
  
**Generated by:** The Cartographer Agent  
**Date:** ${date}  
**Total Organizations:** ${analysis.totalOrganizations}  

## 📊 Overview

| Metric | Count | Percentage |
|--------|-------|------------|
| **Organizations with URLs** | ${analysis.withUrls} | ${Math.round(analysis.withUrls / analysis.totalOrganizations * 100)}% |
| **Organizations missing URLs** | ${analysis.missingUrls} | ${Math.round(analysis.missingUrls / analysis.totalOrganizations * 100)}% |

## 🚨 Critical Data Gaps

Based on masterlist analysis, the following organizations are missing website URLs and likely have incomplete contact information:

### Organizations Missing Websites (Highest Impact)

| Organization | Missing Fields | Contact Score | Impact Priority |
|--------------|----------------|---------------|-----------------|
`;

  // Add top 50 organizations with missing URLs
  const missingUrlOrgs = analysis.report.filter(o => o.missingFields.includes('Website')).slice(0, 50);
  
  for (const org of missingUrlOrgs) {
    markdown += `| **${org.name}** | Website, Email (likely), Phone (likely), LinkedIn (likely) | 0% | 🔴 Critical |\n`;
  }
  
  markdown += `\n*... and ${analysis.missingUrls - 50} more organizations missing URLs*\n\n`;
  
  markdown += `## 📋 Complete List of Organizations Missing URLs

The following ${analysis.missingUrls} organizations need immediate URL research:

`;
  
  // List all orgs missing URLs
  const allMissingUrls = analysis.report.filter(o => o.missingFields.includes('Website'));
  let count = 1;
  for (const org of allMissingUrls) {
    markdown += `${count}. ${org.name}\n`;
    count++;
  }
  
  markdown += `
## 🎯 Recommended Actions

### Immediate Priority (This Week)
1. **URL Discovery Campaign**: Research and add websites for all ${analysis.missingUrls} organizations
2. **Contact Information Harvest**: For organizations with URLs, extract:
   - Email addresses
   - Phone numbers  
   - LinkedIn company pages
   - Key personnel contacts

### Data Enhancement Strategy
1. **Phase 1**: Focus on organizations marked as "URL needed" in masterlist
2. **Phase 2**: Access Notion database to analyze completeness of organizations with URLs
3. **Phase 3**: Systematic enhancement of all missing fields

## 📈 Impact Analysis

Organizations without websites typically lack all contact information, creating a **75% contact score deficit**. Prioritizing URL discovery will unlock access to other contact methods.

### Field Importance Weights
- **Email**: 25 points (critical for outreach)
- **Website**: 20 points (gateway to all information)  
- **LinkedIn URL**: 15 points (professional networking)
- **Phone**: 15 points (direct contact)
- **City/Region**: 5 points (geographic context)
- **Other fields**: 1-5 points each

---
*Note: This analysis is based on the masterlist data. Full field-by-field analysis requires Notion database access.*
`;
  
  return markdown;
}

